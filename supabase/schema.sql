create or replace function public.consume_rate_limit(p_key text,p_limit integer,p_window_seconds integer)
returns boolean language plpgsql security definer set search_path='' as $$
declare current_count integer;
begin
  if p_key!~'^[0-9a-f]{64}$' or p_limit not between 1 and 10000 or p_window_seconds not between 1 and 86400 then raise exception 'Invalid rate limit parameters';end if;
  if mod(hashtextextended(p_key,1),64)=0 then delete from public.rate_limit_windows where updated_at<now()-interval '7 days';end if;
  perform pg_advisory_xact_lock(hashtextextended(p_key,0));
  insert into public.rate_limit_windows(key_hash,window_started_at,request_count,updated_at)values(p_key,now(),1,now())
  on conflict(key_hash)do update set window_started_at=case when public.rate_limit_windows.window_started_at<=now()-make_interval(secs=>p_window_seconds)then now()else public.rate_limit_windows.window_started_at end,request_count=case when public.rate_limit_windows.window_started_at<=now()-make_interval(secs=>p_window_seconds)then 1 else public.rate_limit_windows.request_count+1 end,updated_at=now()
  returning request_count into current_count;
  return current_count<=p_limit;
end $$;
