create or replace function public.create_meeting_request(p_title text,p_duration integer,p_range_start timestamptz,p_range_end timestamptz,p_timezone text,p_recipient_user uuid,p_recipient_email text,p_recipient_name text,p_send boolean,p_options jsonb,p_token_hash text default null,p_token_expires timestamptz default null)
returns uuid language plpgsql security definer set search_path='' as $$
declare actor uuid:=auth.uid();meeting_id uuid;option jsonb;recipient_name text;recipient_email text;recipient_active uuid;base_versions jsonb;initial_state public.meeting_state:=case when p_send then 'options_sent'::public.meeting_state else 'draft'::public.meeting_state end;
begin
  if actor is null then raise exception 'Authentication required' using errcode='42501';end if;
  if p_duration not between 15 and 480 or p_range_end<=p_range_start or jsonb_array_length(p_options) not between 1 and 5 then raise exception 'Invalid meeting request';end if;
  if p_recipient_user is not null then
    if not exists(select 1 from public.connections where status='accepted' and ((requester_id=actor and addressee_id=p_recipient_user)or(requester_id=p_recipient_user and addressee_id=actor))) then raise exception 'Connection required' using errcode='42501';end if;
    if not exists(select 1 from public.schedule_permissions where owner_id=p_recipient_user and grantee_id=actor and scope='free_busy') then raise exception 'Free busy permission required' using errcode='42501';end if;
    select full_name,email into recipient_name,recipient_email from public.profiles where id=p_recipient_user;
    recipient_active:=case when p_send then p_recipient_user else actor end;
  else
    if p_recipient_email is null or p_token_hash is null or p_token_expires<=now() then raise exception 'External recipient requires an expiring token';end if;
    recipient_name:=p_recipient_name;recipient_email:=p_recipient_email;recipient_active:=case when p_send then null else actor end;
  end if;
  base_versions:=jsonb_build_object(actor::text,(select schedule_version from public.profiles where id=actor));
  if p_recipient_user is not null then base_versions:=base_versions||jsonb_build_object(p_recipient_user::text,(select schedule_version from public.profiles where id=p_recipient_user));end if;
  insert into public.meeting_requests(created_by,title,duration_minutes,range_start,range_end,timezone,state,active_responder,base_schedule_versions)values(actor,p_title,p_duration,p_range_start,p_range_end,p_timezone,initial_state,recipient_active,base_versions)returning id into meeting_id;
  insert into public.meeting_participants(meeting_id,user_id,name,role)select meeting_id,actor,full_name,'organizer' from public.profiles where id=actor;
  insert into public.meeting_participants(meeting_id,user_id,email,name,role)values(meeting_id,p_recipient_user,case when p_recipient_user is null then recipient_email end,recipient_name,'recipient');
  for option in select value from jsonb_array_elements(p_options) loop insert into public.meeting_options(id,meeting_id,start_at,end_at,label,reason,source,created_by)values((option->>'id')::uuid,meeting_id,(option->>'startAt')::timestamptz,(option->>'endAt')::timestamptz,option->>'label',option->>'reason','kairos',actor);end loop;
  if p_recipient_user is null then insert into public.external_booking_tokens(meeting_id,token_hash,expires_at)values(meeting_id,p_token_hash,p_token_expires);if p_send then insert into public.simulated_deliveries(meeting_id,channel,destination_masked)values(meeting_id,'email','***@'||split_part(recipient_email,'@',2));end if;end if;
  insert into public.meeting_transitions(meeting_id,to_state,actor_id,version)values(meeting_id,initial_state,actor,1);return meeting_id;
end $$;
revoke all on function public.create_meeting_request(text,integer,timestamptz,timestamptz,text,uuid,text,text,boolean,jsonb,text,timestamptz) from public;grant execute on function public.create_meeting_request(text,integer,timestamptz,timestamptz,text,uuid,text,text,boolean,jsonb,text,timestamptz) to authenticated;

create or replace function public.respond_to_meeting(p_meeting_id uuid,p_action text,p_option_id uuid default null,p_counter_start timestamptz default null)
returns jsonb language plpgsql security definer set search_path='' as $$
declare actor uuid:=auth.uid();meeting public.meeting_requests%rowtype;role_name text;old_state public.meeting_state;recipient_id uuid;chosen public.meeting_options%rowtype;counter_id uuid;
begin
  if actor is null then raise exception 'Authentication required' using errcode='42501';end if;
  select * into meeting from public.meeting_requests where id=p_meeting_id for update;if not found then raise exception 'Meeting not found' using errcode='42501';end if;
  select role into role_name from public.meeting_participants where meeting_id=p_meeting_id and user_id=actor;if role_name is null then raise exception 'Not a participant' using errcode='42501';end if;
  if meeting.state in('confirmed','declined','expired','cancelled') then raise exception 'Meeting already closed';end if;old_state:=meeting.state;
  if p_action='send' and role_name='organizer' and meeting.state='draft' then select user_id into recipient_id from public.meeting_participants where meeting_id=p_meeting_id and role='recipient';update public.meeting_requests set state='options_sent',active_responder=recipient_id,version=version+1,updated_at=now() where id=p_meeting_id;
  elsif p_action='accept' and role_name='recipient' and meeting.state='options_sent' then if not exists(select 1 from public.meeting_options where id=p_option_id and meeting_id=p_meeting_id)then raise exception 'Invalid option';end if;update public.meeting_requests set state='awaiting_sender_confirmation',selected_option_id=p_option_id,active_responder=created_by,version=version+1,updated_at=now()where id=p_meeting_id;
  elsif p_action='counter' and role_name='recipient' and meeting.state='options_sent' then if p_counter_start is null or p_counter_start<meeting.range_start or p_counter_start+make_interval(mins=>meeting.duration_minutes)>meeting.range_end then raise exception 'Counter outside range';end if;insert into public.meeting_options(meeting_id,start_at,end_at,label,reason,source,created_by)values(p_meeting_id,p_counter_start,p_counter_start+make_interval(mins=>meeting.duration_minutes),to_char(p_counter_start at time zone meeting.timezone,'Dy, FMHH12:MI AM'),'Recipient counteroffer','counter',actor)returning id into counter_id;update public.meeting_requests set state='awaiting_sender_confirmation',selected_option_id=counter_id,active_responder=created_by,version=version+1,updated_at=now()where id=p_meeting_id;
  elsif p_action='decline' and role_name='recipient' and meeting.state='options_sent' then update public.meeting_requests set state='declined',active_responder=null,version=version+1,updated_at=now()where id=p_meeting_id;
  elsif p_action='cancel' and role_name='organizer' then update public.meeting_requests set state='cancelled',active_responder=null,version=version+1,updated_at=now()where id=p_meeting_id;
  elsif p_action='confirm' and role_name='organizer' and meeting.state='awaiting_sender_confirmation' then
    select * into chosen from public.meeting_options where id=meeting.selected_option_id and meeting_id=p_meeting_id;if not found then raise exception 'Selected option missing';end if;
    if exists(select 1 from public.meeting_participants participant join public.profiles profile on profile.id=participant.user_id where participant.meeting_id=p_meeting_id and profile.schedule_version<>coalesce((meeting.base_schedule_versions->>(participant.user_id::text))::integer,-1))then raise exception 'Participant schedule changed' using errcode='40001';end if;
    if exists(select 1 from public.meeting_participants participant join public.calendar_items item on item.user_id=participant.user_id where participant.meeting_id=p_meeting_id and item.status='scheduled' and item.start_at<chosen.end_at and item.end_at>chosen.start_at)then raise exception 'Participant schedule changed' using errcode='40001';end if;
    insert into public.calendar_items(user_id,item_type,title,description,start_at,end_at,timezone,priority,flexibility,normal_duration_minutes,minimum_duration_minutes,category,source_proposal_id)
      select participant.user_id,'event',meeting.title,'Confirmed through Kairos',chosen.start_at,chosen.end_at,meeting.timezone,4,'fixed',meeting.duration_minutes,meeting.duration_minutes,'Meeting',null from public.meeting_participants participant where participant.meeting_id=p_meeting_id and participant.user_id is not null;
    update public.profiles set schedule_version=schedule_version+1 where id in(select user_id from public.meeting_participants where meeting_id=p_meeting_id and user_id is not null);
    update public.meeting_requests set state='confirmed',active_responder=null,version=version+1,updated_at=now()where id=p_meeting_id;
  else raise exception 'Invalid meeting transition';end if;
  select * into meeting from public.meeting_requests where id=p_meeting_id;insert into public.meeting_transitions(meeting_id,from_state,to_state,actor_id,version,metadata)values(p_meeting_id,old_state,meeting.state,actor,meeting.version,jsonb_build_object('action',p_action));return jsonb_build_object('meeting_id',p_meeting_id,'state',meeting.state,'version',meeting.version);
end $$;
revoke all on function public.respond_to_meeting(uuid,text,uuid,timestamptz) from public;grant execute on function public.respond_to_meeting(uuid,text,uuid,timestamptz) to authenticated;

create or replace function public.respond_to_external_booking(p_token_hash text,p_action text,p_option_id uuid default null,p_counter_start timestamptz default null)
